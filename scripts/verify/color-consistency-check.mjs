async function openScatterGroup(page, requestedGroup = '') {
    await page.waitForSelector('[data-role="xy-point"]', { state: 'attached' });
    const group = await page.evaluate(groupName => {
        const points = [...document.querySelectorAll('[data-role="xy-point"]')];
        const point = (groupName
            ? points.find(el => el.getAttribute('data-bar-group') === groupName)
            : points[0]);
        if (!point || !point.nextElementSibling)
            throw new Error('Scatter point or hit halo not found');
        const selected = point.getAttribute('data-bar-group') || '';
        point.nextElementSibling.dispatchEvent(new MouseEvent('click', { bubbles: true }));
        return selected;
    }, requestedGroup);
    await page.waitForSelector('[data-ps-scope="group"]', { timeout: 5000 });
    return group;
}

async function scatterGroupState(page, group) {
    return page.evaluate(groupName => {
        const points = [...document.querySelectorAll(
            '[data-role="xy-point"][data-bar-group="' + CSS.escape(groupName) + '"]')];
        const data = window.gb2_undo && window.gb2_undo.getData
            ? window.gb2_undo.getData() : {};
        return {
            count: points.length,
            fills: [...new Set(points.map(el => (el.getAttribute('fill') || '').toLowerCase()))],
            tags: [...new Set(points.map(el => el.tagName.toLowerCase()))],
            groupColors: Array.isArray(data.groupColors) ? data.groupColors : [],
            pointShapes: Array.isArray(data.xyPointShapes) ? data.xyPointShapes : [],
            pointGroupStyles: (() => {
                try { return JSON.parse(data.xyPointGroupStyles || '{}'); }
                catch { return {}; }
            })()
        };
    }, group);
}

await withRenderedPage('xy_facet', 'Scatter Use palette', async page => {
    const group = await openScatterGroup(page);
    const before = await scatterGroupState(page, group);
    const original = before.fills[0] || '';
    const paletteChoices = await page.locator('[data-ps-palette-target="p-color"]')
        .evaluateAll(buttons => buttons.map(button => button.getAttribute('data-ps-palette')));
    const custom = paletteChoices.find(color => color && color !== 'transparent' &&
        color.toLowerCase() !== original);
    if (!custom) throw new Error('No alternate scatter quick color found');
    await page.locator('[data-ps-palette="' + custom + '"]' +
        '[data-ps-palette-target="p-color"]').click();
    await page.waitForTimeout(40);
    const customized = await scatterGroupState(page, group);
    check('Scatter This group quick color creates a visible literal override',
        customized.fills.length === 1 && customized.fills[0] === custom.toLowerCase() &&
        customized.groupColors.some(entry => String(entry.original) === group &&
            String(entry.color).toLowerCase() === custom.toLowerCase()),
        JSON.stringify(customized));

    // Seed the legacy secondary store too. It must be deleted by the same
    // reachable action, and the immediate repaint must happen after both
    // stores are clear (not only after groupColors is cleared).
    const secondary = paletteChoices.find(color => color && color !== 'transparent' &&
        color.toLowerCase() !== original && color.toLowerCase() !== custom.toLowerCase());
    if (!secondary) throw new Error('No secondary scatter quick color found');
    await page.evaluate(({ groupName, color }) => {
        const data = window.gb2_undo.getData();
        let styles = {};
        try { styles = JSON.parse(data.xyPointGroupStyles || '{}'); } catch { styles = {}; }
        styles[groupName] = { ...(styles[groupName] || {}), color };
        data.xyPointGroupStyles = JSON.stringify(styles);
        window.__gb2_lastRenderedHash = null;
        window.GraphBuilder2.render(document.querySelector('.graphbuilder2-host').id, data);
    }, { groupName: group, color: secondary });
    await page.waitForTimeout(80);
    await openScatterGroup(page, group);
    const shadowed = await scatterGroupState(page, group);
    check('Legacy scatter point-style color visibly shadows the group color before clearing',
        shadowed.fills.length === 1 && shadowed.fills[0] === secondary.toLowerCase(),
        JSON.stringify(shadowed));

    await page.locator('[data-field="p-color-use-palette"]').click();
    await page.waitForTimeout(40);
    const cleared = await scatterGroupState(page, group);
    check('Scatter Use palette deletes the group color override and restores palette color',
        !cleared.groupColors.some(entry => String(entry.original) === group) &&
        !(cleared.pointGroupStyles[group] && cleared.pointGroupStyles[group].color != null) &&
        cleared.fills.length === 1 && cleared.fills[0] === original,
        JSON.stringify(cleared));

    await page.evaluate(() => {
        const data = window.gb2_undo.getData();
        data.chartPalette = 'okabe-ito';
        window.__gb2_lastRenderedHash = null;
        window.GraphBuilder2.render(document.querySelector('.graphbuilder2-host').id, data);
    });
    await page.waitForTimeout(80);
    const followed = await scatterGroupState(page, group);
    check('Cleared scatter group follows a subsequent palette change',
        !followed.groupColors.some(entry => String(entry.original) === group) &&
        followed.fills.length === 1 && followed.fills[0] !== original &&
        followed.fills[0] !== custom.toLowerCase(),
        JSON.stringify(followed));
});

