async function withRenderedPage(file, label, fn) {
    const context = await browser.newContext({ viewport: { width: 1100, height: 1200 } });
    const page = await context.newPage();
    const pageErrors = [];
    page.on('pageerror', error => pageErrors.push(error.message));
    try {
        await page.goto('file://' + path.join(OUT, file + '.html'));
        await page.waitForSelector('svg', { timeout: 10000 });
        await loadLatestWidget(page);
        await fn(page);
        check(label + ' has no page errors', pageErrors.length === 0, pageErrors.join(' | '));
    } catch (error) {
        check(label + ' rendered route completes', false,
            String(error && error.stack ? error.stack : error));
    } finally {
        await context.close();
    }
}

