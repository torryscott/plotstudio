let failures = 0;

function check(label, pass, detail = '') {
    console.log((pass ? ' ok  ' : 'FAIL ') + label + (detail ? ': ' + detail : ''));
    if (!pass) failures++;
}
function section(start, end) {
    const a = source.indexOf(start);
    const b = source.indexOf(end, a + start.length);
    return (a >= 0 && b > a) ? source.slice(a, b) : '';
}
function count(re, text = source) {
    return (text.match(re) || []).length;
}

function loadPlaywright() {
    const bases = [process.env.GB2_NODE_BASE, process.cwd(), '/tmp', '/private/tmp'].filter(Boolean);
    for (const base of bases) {
        try { return createRequire(path.join(base, 'x.js'))('playwright'); }
        catch { /* try the next workspace/runtime */ }
    }
    throw new Error('playwright not found (set GB2_NODE_BASE)');
}

async function loadLatestWidget(page) {
    // Fixtures are generated by the full verification runner, but injecting
    // the workspace source here also makes this focused check trustworthy
    // when it is run directly after an edit.
    await page.addScriptTag({ content: source });
    await page.evaluate(() => {
        let payload = window.__gb2_payload;
        if (!payload) {
            const script = [...document.scripts].find(s =>
                s.textContent.includes('var __gb2_payload = '));
            const text = script?.textContent || '';
            const marker = 'var __gb2_payload = ';
            const start = text.indexOf(marker) + marker.length;
            const end = text.indexOf(';\nvar __gb2_id =', start);
            if (start < marker.length || end < 0)
                throw new Error('Embedded GraphBuilder payload not found');
            payload = JSON.parse(text.slice(start, end));
        }
        payload = JSON.parse(JSON.stringify(payload));
        window.__gb2_payload = payload;
        window.__gb2_lastRenderedHash = null;
        const host = document.querySelector('.graphbuilder2-host');
        if (!host || !window.GraphBuilder2) throw new Error('GraphBuilder host unavailable');
        window.GraphBuilder2.render(host.id, payload);
    });
    await page.waitForTimeout(80);
}

async function clickAxisLine(page, axis) {
    const probe = await page.evaluate(axisName => {
        const title = axisName === 'x'
            ? 'Click to open X-axis settings' : 'Click to open Y-axis settings';
        const hit = [...document.querySelectorAll('div[title="' + title + '"]')]
            .find(el => {
                const r = el.getBoundingClientRect();
                const cs = getComputedStyle(el);
                return r.width > 0 && r.height > 0 &&
                    cs.display !== 'none' && cs.visibility !== 'hidden';
            });
        if (!hit) return null;
        const r = hit.getBoundingClientRect();
        return { x: r.left + r.width / 2, y: r.top + r.height / 2 };
    }, axis);
    if (!probe) throw new Error(axis.toUpperCase() + '-axis hit target not found');
    await page.mouse.click(probe.x, probe.y);
    await page.waitForSelector('[data-' + axis + 'a-tab="line"]',
        { state: 'attached', timeout: 5000 });
}

